async function main() {
  console.log('Seeding Paragraf Koçu Denemeleri - question-outcome mappings');

  const book = await prisma.book.findFirst({
    where: { title: BOOK_TITLE },
    select: { id: true },
  });
  if (!book) {
    throw new Error(`Book "${BOOK_TITLE}" not found. Run seed:denemeler and seed:denemeler:structure first.`);
  }
  const bookId = book.id;

  const outcomes = await prisma.learningOutcome.findMany({
    where: { bookId },
    select: { id: true, code: true },
  });
  const outcomeMap = new Map(outcomes.map((o: any) => [o.code, o.id]));

  const tests = await prisma.test.findMany({
    where: { section: { bookId } },
    select: { id: true, title: true },
  });

  const testByNo = new Map<number, string>();
  for (const t of tests) {
    const n = parseInt(t.title.replace(/\D/g, ''), 10);
    if (!isNaN(n)) testByNo.set(n, t.id);
  }

  // Eski mapping'leri sil (bu kitaba ait sorular için)
  const deleted = await prisma.questionOutcome.deleteMany({
    where: { question: { test: { section: { bookId } } } },
  });
  console.log(`✓ Removed ${deleted.count} old mappings`);

  let mappingCount = 0;
  let skippedCount = 0;

  for (const row of RAW_MAPPINGS) {
    const testId = testByNo.get(row.testNo);
    if (!testId) {
      console.warn(`⚠ Test not found: TEST ${row.testNo}`);
      skippedCount++;
      continue;
    }

    const questionIndex = row.questionNo - 1;
    const question = await prisma.question.findFirst({
      where: { testId, orderIndex: questionIndex },
      select: { id: true },
    });

    if (!question) {
      console.warn(`⚠ Question not found: TEST ${row.testNo} Q${row.questionNo}`);
      skippedCount++;
      continue;
    }

    const outcomeId = outcomeMap.get(row.code);
    if (!outcomeId) {
      console.warn(`⚠ Outcome not found for code: ${row.code}`);
      skippedCount++;
      continue;
    }

    await prisma.questionOutcome.create({
      data: {
        questionId: question.id,
        learningOutcomeId: outcomeId,
      },
    });
    mappingCount++;
  }

  console.log(`✓ ${mappingCount} question-outcome mappings created`);
  if (skippedCount > 0) console.log(`⚠ ${skippedCount} skipped`);

  const totalMappings = await prisma.questionOutcome.count({
    where: { question: { test: { section: { bookId } } } },
  });
  console.log('\n=== VALIDATION ===');
  console.log(`Mappings for "${BOOK_TITLE}": ${totalMappings}`);
  console.log('✓ Done');
}

main()
  .catch((e) => {
    console.error(e);
    process.exit(1);
  })
  .finally(() => prisma.$disconnect());
